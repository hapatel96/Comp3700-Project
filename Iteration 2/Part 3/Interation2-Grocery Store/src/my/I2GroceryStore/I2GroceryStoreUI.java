   private void cashierSignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cashierSignInActionPerformed
        Pair<String> UserPair = new Pair<>(userNameEntry.getText(), String.valueOf(passwordEntry.getPassword()));

        for (Pair<String> cashier : cashiers) {
          if(cashier.equals(UserPair)){
            signInPanel.setVisible(false);
            cashierPanel.setVisible(true);
            clearMessages();
            currentUser = cashiers.indexOf(cashier);
            return;
          }
        }
      //if we're here, bad username/password combo
      invalidCredentialsMessage.setVisible(true);
   }//GEN-LAST:event_cashierSignInActionPerformed

