    private void checkNamePassChange() {
      String newName = userNameField.getText();
      String newPass = passwordField.getText();
      if(!(newName.equals(""))){
          String oldPass;
          if(hasManager){
              oldPass = managers.get(currentUser).getValue();
              managers.set(currentUser, new Pair<>(newName, oldPass));
          }
          else{
              oldPass = cashiers.get(currentUser).getValue();
              cashiers.set(currentUser, new Pair<>(newName, oldPass));
          }
      }
      if(!(newPass.equals(""))){
          String oldName;
          if(hasManager){
              oldName = managers.get(currentUser).getKey();
              managers.set(currentUser, new Pair<>(oldName, newPass));
          }
          else{
              oldName = cashiers.get(currentUser).getKey();
              cashiers.set(currentUser, new Pair<>(oldName, newPass));
          }
      }
    }
   
   private void populateProfileList() {
        ((DefaultTableModel) ManProfileList.getModel()).setRowCount(0);
        DefaultTableModel profileList = (DefaultTableModel) ManProfileList.getModel();
        for(Pair<String> manager : managers){
            profileList.addRow(
                new Object[]{manager.getKey(),"Manager"}
            );
        }
        for(Pair<String> cashier : cashiers){
            profileList.addRow(
                new Object[]{cashier.getKey(),"Cashier"}
            );
        }
   }
   
